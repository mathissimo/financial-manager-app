    public void initAusgabenTypEditor (){
        myModel=DshApp.getModel();
        myAusgabenTyp = new AusgabenTyp ();
        refreshATtable();
        loadATintoEditor();
    }
    
    private void refreshATtable () {
        myATtable=myModel.getTableAusgabenTyp();
        if (myATtable!=null) {
            tableExistingAusgbenTypen.setModel(myATtable);
            tableExistingAusgbenTypen.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            TableModelHelper.packColumns(tableExistingAusgbenTypen);
        } else {
            JOptionPane.showMessageDialog(this, "Tabelle Ausgabe-Typ konnte NICHT geladen werden!");
        }
    }
    
    private void loadATintoEditor (){
        if (myAusgabenTyp.getId()==-1 || myAusgabenTyp.isAusgabenTypSuccesfullyLoaded()) {
            textFieldAusgabenTypName.setText(myAusgabenTyp.getAtName());
            checkBoxBudgetNeutral.setSelected(myAusgabenTyp.isBudgetNeutral());
        } else {
            JOptionPane.showMessageDialog(this, "Ausgabe-Typ konnte NICHT geladen werden!");
        }       
    }
    
    private String errorOnFillingEntriesIntoAT () {
        String entriesValidationError = "";
        if (textFieldAusgabenTypName.getText().equals("")) entriesValidationError="Bezeichnerfeld ist leer.";
        return entriesValidationError;
    }
    private boolean succesfullySavedDataToDB () {
        String fieldsErrorCode = errorOnFillingEntriesIntoAT(); // Eintr채ge valide?
        if (fieldsErrorCode.equals("")) { 
            myAusgabenTyp.setAtName(textFieldAusgabenTypName.getText());
            myAusgabenTyp.setBudgetNeutral(checkBoxBudgetNeutral.isSelected());
            return myAusgabenTyp.successfullySavedToDB(); // Budget Objekt k체mmert sich ums speichern
        } else {
           JOptionPane.showMessageDialog(this, fieldsErrorCode+": Bitte 체berpr체fen Sie Ihre Eingabe.");
           System.out.println ("** Eingabefehler: "+fieldsErrorCode);
        }
        return false;
    } 

