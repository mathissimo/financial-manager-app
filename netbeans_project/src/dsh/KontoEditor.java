    public void initKontoEditor (){
        myModel=DshApp.getModel();
        myKonto = new Konto ();
        refreshKontotable();
        loadKontointoEditor();
    }
    
    private void refreshKontotable () {
        myKontotable=myModel.getTableKonto();
        if (myKontotable!=null) {
            tableExistingKonten.setModel(myKontotable);
            tableExistingKonten.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            TableModelHelper.packColumns(tableExistingKonten);
        } else {
            JOptionPane.showMessageDialog(this, "Tabelle Ausgabe-Typ konnte NICHT geladen werden!");
        }
    }
    
    private void loadKontointoEditor (){
        if (myKonto.getId()==-1 || myKonto.isKontoSuccesfullyLoaded()) {
            textFieldKontoName.setText(myKonto.getKontoName());
            checkBoxEigentum.setSelected(myKonto.isEigentum());
            checkBoxPositiv.setSelected(myKonto.isPositiv());
        } else {
            JOptionPane.showMessageDialog(this, "Ausgabe-Typ konnte NICHT geladen werden!");
        }       
    }
    
    private String errorOnFillingEntriesIntoKonto () {
        String entriesValidationError = "";
        if (textFieldKontoName.getText().equals("")) entriesValidationError="Bezeichnerfeld ist leer.";
        return entriesValidationError;
    }
    private boolean succesfullySavedDataToDB () {
        String fieldsErrorCode = errorOnFillingEntriesIntoKonto(); // Eintr채ge valide?
        if (fieldsErrorCode.equals("")) { 
            myKonto.setKontoName(textFieldKontoName.getText());
            myKonto.setEigentum(checkBoxEigentum.isSelected());
            myKonto.setPositiv(checkBoxPositiv.isSelected());
            return myKonto.successfullySavedToDB(); // Budget Objekt k체mmert sich ums speichern
        } else {
           JOptionPane.showMessageDialog(this, fieldsErrorCode+": Bitte 체berpr체fen Sie Ihre Eingabe.");
           System.out.println ("** Eingabefehler: "+fieldsErrorCode);
        }
        return false;
    } 

